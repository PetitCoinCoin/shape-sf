											<li>Le bon état général des selles employées actuellement</li>
											<li>L’adéquation de la selle actuelle au dos du cheval en statique et en dynamique (monté)</li>
											<li>L’adaptation si possible de la selle (arcade & panneaux, correcteur d’équilibre, etc)</li>
											<li>L’adéquation de la selle au cavalier (essais de divers sièges pour donner des sensations différentes)</li>
